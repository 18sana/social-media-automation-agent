/**
 * Simulated LangGraph Engine
 * Models a state machine with tools, conditional routing, and Human-In-The-Loop checkpoints.
 */

class AgentWorkflowEngine {
    constructor(onStateChange, onLog) {
        this.onStateChange = onStateChange; // Callback when active node changes
        this.onLog = onLog;                 // Callback when a log is emitted
        this.currentState = null;
        this.isRunning = false;
        this.activeNode = null;
    }

    // Emit standard terminal format log
    log(message, level = "INFO") {
        const timestamp = new Date().toLocaleTimeString();
        this.onLog({ time: timestamp, level, message });
    }

    // Sleep helper for animations
    sleep(ms) {
        return new Promise(resolve => setTimeout(resolve, ms));
    }

    /**
     * Run the workflow end-to-end
     * Config parameters: { topic, platform, tone, customPrompt }
     */
    async run(config) {
        if (this.isRunning) return;
        this.isRunning = true;
        this.log(`Initiating LangGraph compilation for: [Platform: ${config.platform.toUpperCase()} | Topic: "${config.topic}"]`, "INFO");
        
        try {
            // Node 1: START
            await this.transitionTo("START");
            this.log("Executing StateGraph init. State schema loaded successfully.", "INFO");
            await this.sleep(1200);

            // Node 2: WEB_SEARCH (Tool Call)
            await this.transitionTo("WEB_SEARCH");
            this.log(`Invoking SearchTool with query: "latest trends in ${config.topic}"`, "TOOL");
            await this.sleep(1800);
            this.log(`SearchTool returned 3 relevant documents. Synthesizing context...`, "SUCCESS");
            await this.sleep(800);

            // Node 3: IMAGE_GEN (Tool Call)
            await this.transitionTo("IMAGE_GEN");
            this.log(`Invoking VisualGeneratorTool. Fabricating layout ideas for platform [${config.platform}]...`, "TOOL");
            await this.sleep(2000);
            const mockMediaUrl = this.getRandomMediaUrl(config.platform);
            this.log(`VisualGeneratorTool successfully matched media layout asset.`, "SUCCESS");
            await this.sleep(800);

            // Node 4: DRAFT_POST
            await this.transitionTo("DRAFT_POST");
            this.log(`Invoking CopywriterAgent using LLM model (Gemini 3.5 Flash). Tone: ${config.tone}`, "AGENT");
            await this.sleep(2200);
            
            // Generate mock post content based on prompt
            const generatedContent = this.generateSyntheticContent(config);
            this.log(`CopywriterAgent generated draft successfully. Schema validation passed.`, "SUCCESS");
            await this.sleep(1000);

            // Node 5: SLACK_HITL (Human-in-the-Loop Interrupted State)
            await this.transitionTo("SLACK_HITL");
            this.log(`Interrupt encountered at checkpoint 'slack_hitl'. Graph execution suspended.`, "WARNING");
            this.log(`Serializing state variables and posting notification payload to Slack channel #social-approvals.`, "INFO");
            
            this.isRunning = false;
            
            // Return generated draft awaiting human review
            return {
                id: "temp-" + Date.now(),
                platform: config.platform,
                content: generatedContent.text,
                mediaType: "image",
                mediaUrl: mockMediaUrl,
                mediaIdea: generatedContent.mediaIdea,
                status: "review",
                scheduledTime: new Date(Date.now() + 24 * 60 * 60 * 1000).toISOString().slice(0, 16),
                engagement: { likes: 0, comments: 0, shares: 0 },
                logs: ["Post generated by autonomous LangGraph engine.", "Awaiting human-in-the-loop review."]
            };

        } catch (error) {
            this.log(`Workflow compilation failed: ${error.message}`, "ERROR");
            this.isRunning = false;
            await this.transitionTo(null);
            throw error;
        }
    }

    // Simulates the revision phase when user edits or rejects a post
    async runRevision(post, feedback) {
        if (this.isRunning) return;
        this.isRunning = true;
        this.log(`Re-entering LangGraph state machine from HITL feedback interrupt node.`, "INFO");
        this.log(`User feedback registered: "${feedback}"`, "WARNING");

        try {
            await this.transitionTo("REVISION");
            this.log(`Invoking CopywriterAgent revision workflow on [Post ID: ${post.id}].`, "AGENT");
            await this.sleep(2000);

            // Synthesize adjusted text
            const revisedText = `✨ Revised Post: ${post.content}\n\n[Incorporated Feedback: ${feedback}]`;
            this.log(`CopywriterAgent resolved feedback. Validation check ok.`, "SUCCESS");
            await this.sleep(1000);

            await this.transitionTo("SLACK_HITL");
            this.log(`Graph execution suspended at checkpoint 'slack_hitl'. Ready for approval.`, "WARNING");
            
            this.isRunning = false;
            return {
                ...post,
                content: revisedText,
                logs: [...post.logs, `Feedback revision executed: "${feedback}"`]
            };
        } catch (error) {
            this.log(`Revision routing failed: ${error.message}`, "ERROR");
            this.isRunning = false;
            await this.transitionTo(null);
        }
    }

    // Node transition visual updates
    async transitionTo(nodeId) {
        this.activeNode = nodeId;
        this.onStateChange(nodeId);
        if (nodeId) {
            this.log(`Graph transitioned to active node: [${nodeId}]`, "INFO");
        }
    }

    // Simulated content generation
    generateSyntheticContent(config) {
        const topics = {
            "Modern Front-end Aesthetics": {
                text: `🎨 Aesthetics build software authority!\n\nWhen creating next-generation web applications, developers must avoid generic templates. Premium dark mode palettes, soft glassmorphism, responsive grids, and micro-interactions elevate a simple utility into an immersive portal. \n\nWe design to WOW at first glance. ✨🚀\n\n#FrontEnd #Aesthetics #UXUI #WebDevelopment #CodingStandards`,
                mediaIdea: "A dark high-fidelity interface mockup featuring glowing purple highlights and elegant frosted overlays."
            },
            "Model Context Protocol": {
                text: `🔗 Say goodbye to hardcoded API endpoints and custom integrations for every AI model. Standardizing on the Model Context Protocol (MCP) gives agents a secure, modular, and governed tool calling interface.\n\nSeparating system runtimes from context generation layer is the architecture model of 2026. 🛠️💻\n\n#AI #APIIntegration #ModelContextProtocol #SoftwareArchitecture`,
                mediaIdea: "An abstract diagram illustrating isolated LLM cores communicating via safe JSON-RPC bridges to external databases."
            },
            "LangGraph State Machines": {
                text: `🔄 Autonomous AI agents without limits! \n\nBy leveraging LangGraph's cyclic state graphs, you can build reliable multi-agent systems with built-in Human-In-The-Loop (HITL) checkpoints. Define clear state variables, invoke custom tool schemas, and maintain absolute execution control.\n\nHere is how to design state graphs: 👇🤖\n\n#LangGraph #AIAgent #StateMachines #SoftwareEngineering`,
                mediaIdea: "A glowing flowchart highlighting routing decisions and automated tool gateways."
            }
        };

        const defaultContent = {
            text: `📈 Let's dive deep into "${config.topic}". \n\nTo build highly scalable applications, engineering teams must maintain precise governance protocols. Our latest research highlights that incorporating tool-calling architectures is critical to standardizing operational workflow speeds.\n\nHow is your team handling this? 💡👇\n\n#Innovation #TechTrends #ProductManagement`,
            mediaIdea: "A modern line graph indicating accelerated growth rates."
        };

        return topics[config.topic] || defaultContent;
    }

    getRandomMediaUrl(platform) {
        const urls = [
            "https://images.unsplash.com/photo-1618005182384-a83a8bd57fbe?q=80&w=800&auto=format&fit=crop",
            "https://images.unsplash.com/photo-1507238691740-187a5b1d37b8?q=80&w=800&auto=format&fit=crop",
            "https://images.unsplash.com/photo-1526374965328-7f61d4dc18c5?q=80&w=800&auto=format&fit=crop",
            "https://images.unsplash.com/photo-1550751827-4bd374c3f58b?q=80&w=800&auto=format&fit=crop"
        ];
        return urls[Math.floor(Math.random() * urls.length)];
    }
}
